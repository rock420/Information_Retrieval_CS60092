ordered_headers_list = []
for headers in headers_list:
    h = OrderedDict()
    for header,value in headers.items():
        h[header]=value
    ordered_headers_list.append(h)


def generate_random_header():
    header = random.choice(headers_list)
    user_agent = random.choice(user_agent_list)
    header['User-Agent'] = user_agent
    return header


def grab_page(url):
    print("attempting to grab page: " + url)
    header = generate_random_header()
    page = requests.get(url,headers=header)
    page_html = page.text
    soup = BeautifulSoup(page_html, 'html.parser')

    #meta = soup.find("div",{'class':'a-info get-alerts'})
    filename = str(url).split("/")[-1]+".html"
    html_content = soup.find("div",id='a-body')
    if html_content==None:
        print("skipping this link, no content here")
        return
    with open("./ECT/"+filename, 'w') as f:
        f.write(str(html_content))
    

def process_list_page(i):
    origin_page = "https://seekingalpha.com/earnings/earnings-call-transcripts/{}".format(i)
    print("\ngetting page " + origin_page)
    header = generate_random_header()
    page = requests.get(origin_page,headers=header)
    page_html = page.text
    soup = BeautifulSoup(page_html, 'html.parser')
    alist = soup.find_all("li",{'class':'list-group-item article'})
    for i in range(0,len(alist)):
        url_ending = alist[i].find_all("a")[0].attrs['href']
        url = "https://seekingalpha.com" + url_ending
        grab_page(url)
        time.sleep(.1)


for i in range(1,500): #choose what pages of earnings to scrape
    process_list_page(i)